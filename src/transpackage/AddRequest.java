    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (CN.getText().compareTo("") == 0) {
            JOptionPane.showMessageDialog(null, " name  is empty");
        } else if (TL.getText().length() < 10) {
            JOptionPane.showMessageDialog(null, "tel no  is incorrect");
        } else if (AD.getText().compareTo("") == 0) {
            JOptionPane.showMessageDialog(null, "Address is empty");
        } else if (DAT.getText().compareTo("") == 0) {
            JOptionPane.showMessageDialog(null, "date is empty");

        } else {
            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/teadb", "root", "");
                String query = "insert into details1(customerName,telNO,address,availability,date,description)values(?,?,?,?,?,?)";
                PreparedStatement pst = con.prepareStatement(query);
                pst.setString(1, CN.getText());
                pst.setString(2, TL.getText());
                pst.setString(3, AD.getText());

                String ava;
                ava = G.getSelectedItem().toString() + "\n" + B.getSelectedItem().toString() + "\n" + A.getSelectedItem().toString();
                pst.setString(4, ava);
                pst.setString(5, DAT.getText());
                pst.setString(6, DES.getText());

                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Insert Sucessfully");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);

            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

